// Example 6: Prompt Templates
// ============================================================================

async function promptTemplateExample() {
  const engine = createOrchestrationEngine({
    enablePromptEngine: true,
  });

  const promptEngine = (engine as any).promptEngine;

  // Render a prompt template
  const templateId = 'question-answering';
  const variables = {
    context: 'Python is a high-level programming language...',
    question: 'What is Python?',
    constraints: 'Keep the answer under 100 words',
  };

  const renderedPrompt = promptEngine.render(templateId, variables);
  console.log('Rendered prompt:', renderedPrompt);

  // Use the rendered prompt
  const request: LLMRequest = {
    messages: [
      { role: 'user', content: renderedPrompt },
    ],
  };

  await engine.execute(request);

  // Optimize the template
  const optimizationResult = await promptEngine.optimize(templateId);
  console.log('Optimization suggestions:', optimizationResult.suggestions);

  engine.dispose();
}

