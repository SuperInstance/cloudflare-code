// Example 3: Budget Management
// ============================================================================

async function budgetExample() {
  const engine = createOrchestrationEngine({
    enableCostTracking: true,
  });

  // Create a budget
  const dailyBudget: BudgetConfig = {
    id: 'daily-budget',
    name: 'Daily API Budget',
    limit: 10, // $10 per day
    period: 'daily',
    scope: 'global',
    alertThreshold: 0.8, // Alert at 80%
    actions: [
      {
        type: 'alert',
        threshold: 0.8,
      },
      {
        type: 'throttle',
        threshold: 0.9,
      },
    ],
  };

  engine.createBudget(dailyBudget);

  // Execute requests with budget tracking
  for (let i = 0; i < 10; i++) {
    try {
      const request: LLMRequest = {
        messages: [
          { role: 'user', content: `Question ${i}` },
        ],
      };

      await engine.execute(request, {
        userId: 'user-123',
      });
    } catch (error: any) {
      if (error.code === 'BUDGET_EXCEEDED') {
        console.log('Budget exceeded, stopping requests');
        break;
      }
    }
  }

  // Check budget spending
  const costOptimizer = (engine as any).costOptimizer;
  const spending = costOptimizer.getBudgetSpending('daily-budget');
  console.log('Total spending:', spending);

  engine.dispose();
}

// ============================================================================
// Example 4: Rate Limiting
// ============================================================================

async function rateLimitExample() {
  const engine = createOrchestrationEngine({
    enableRateLimiting: true,
  });

  // Create rate limit quota
  const userQuota: RateLimitQuota = {
    id: 'free-tier-quota',
    name: 'Free Tier Quota',
    scope: 'user',
    scopeId: 'user-123',
    limits: {
      requests: 100,
      tokens: 50000,
      window: 3600000, // 1 hour
    },
    priority: 1,
  };

  engine.createRateLimit(userQuota);

  // Execute requests with rate limiting
  for (let i = 0; i < 150; i++) {
    try {
      const request: LLMRequest = {
        messages: [
          { role: 'user', content: `Request ${i}` },
        ],
      };

      await engine.execute(request, {
        userId: 'user-123',
      });

      console.log(`Request ${i} succeeded`);
    } catch (error: any) {
      if (error.code === 'RATE_LIMIT_EXCEEDED') {
        console.log(`Rate limit exceeded at request ${i}`);
        console.log('Resets at:', new Date(error.details.resetTime));
        break;
      }
    }
  }

  engine.dispose();
}

