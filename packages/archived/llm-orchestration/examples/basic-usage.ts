// Example 5: Multi-Model Aggregation
// ============================================================================

async function aggregationExample() {
  const engine = createOrchestrationEngine({
    enableAggregation: true,
  });

  // Complex question requiring multiple perspectives
  const request: LLMRequest = {
    messages: [
      {
        role: 'user',
        content: 'What are the pros and cons of microservices architecture?',
      },
    ],
  };

  // Aggregate responses from multiple models
  const response = await engine.execute(request, {
    multiModel: true,
    aggregation: {
      method: 'consensus',
      strategy: 'majority',
      threshold: 0.7,
    },
  });

  console.log('Aggregated response:', response.choices[0].message.content);
  console.log('Consensus level:', (response.metadata as any).aggregated.consensus);

  engine.dispose();
}

