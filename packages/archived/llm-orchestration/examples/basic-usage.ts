// Example 9: Error Handling
// ============================================================================

async function errorHandlingExample() {
  const engine = createOrchestrationEngine({
    enableFallback: true,
    maxRetries: 3,
  });

  // Handle different error types
  try {
    const request: LLMRequest = {
      messages: [
        { role: 'user', content: 'Help me write code' },
      ],
    };

    const response = await engine.execute(request, {
      routing: {
        fallbackEnabled: true,
        fallbackModels: ['gpt-3.5-turbo', 'claude-3-haiku'],
      },
    });

    console.log('Response:', response.choices[0].message.content);
  } catch (error: any) {
    switch (error.code) {
      case 'MODEL_UNAVAILABLE':
        console.log('Model unavailable, try again later');
        break;
      case 'RATE_LIMIT_EXCEEDED':
        console.log('Rate limited, retry after:', error.details.resetTime);
        break;
      case 'BUDGET_EXCEEDED':
        console.log('Budget exceeded, limit:', error.details.limit);
        break;
      default:
        console.log('Unexpected error:', error.message);
    }
  }

  engine.dispose();
}

