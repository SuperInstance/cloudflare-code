async function eventHandlingExample() {
  const engine = createOrchestrationEngine();

  // Listen to events
  engine.on('request:start', (data: any) => {
    console.log('Request started:', data.requestId);
  });

  engine.on('routing:decision', (data: any) => {
    console.log('Routing decision:', data.decision.model);
  });

  engine.on('request:complete', (data: any) => {
    console.log('Request completed in', data.duration, 'ms');
  });

  engine.on('budget:exceeded', (data: any) => {
    console.log('Budget exceeded:', data.budget);
  });

  engine.on('rate-limit:exceeded', (data: any) => {
    console.log('Rate limit exceeded:', data.scope);
  });

  // Execute a request
  const request: LLMRequest = {
    messages: [
      { role: 'user', content: 'Hello' },
    ],
  };

  await engine.execute(request);

  engine.dispose();
}

