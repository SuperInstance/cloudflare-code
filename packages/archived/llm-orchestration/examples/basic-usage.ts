// Example 2: Intelligent Routing
// ============================================================================

async function routingExample() {
  const engine = createOrchestrationEngine({
    enableRouting: true,
  });

  // Code generation request - routes to best code model
  const codeRequest: LLMRequest = {
    messages: [
      {
        role: 'user',
        content: 'Write a function to validate email addresses in JavaScript',
      },
    ],
  };

  const codeResponse = await engine.execute(codeRequest, {
    routing: {
      strategy: 'capability',
      capabilityRequirements: {
        codeGeneration: { supported: true, confidence: 0.9 },
      },
    },
  });

  console.log('Code generated by model:', codeResponse.model);

  // Cost-optimized request
  const simpleRequest: LLMRequest = {
    messages: [
      { role: 'user', content: 'What is 2 + 2?' },
    ],
  };

  const costOptimizedResponse = await engine.execute(simpleRequest, {
    routing: {
      strategy: 'cost',
      costLimit: 5,
    },
  });

  console.log('Cost-optimized response from:', costOptimizedResponse.model);

  engine.dispose();
}

