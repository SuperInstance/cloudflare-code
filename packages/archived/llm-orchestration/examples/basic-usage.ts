async function advancedConfigExample() {
  const engine = createOrchestrationEngine({
    enableRouting: true,
    enableAggregation: true,
    enablePromptEngine: true,
    enableCostTracking: true,
    enableRateLimiting: true,
    enableCaching: true,
    defaultRoutingStrategy: 'weighted',
    defaultAggregationMethod: 'consensus',
    cacheTTL: 3600000, // 1 hour
    maxRetries: 3,
    timeout: 30000, // 30 seconds
  });

  // Create budgets
  const dailyBudget: BudgetConfig = {
    id: 'daily',
    name: 'Daily Budget',
    limit: 50,
    period: 'daily',
    scope: 'global',
    hardLimit: false,
  };

  const userBudget: BudgetConfig = {
    id: 'user-123',
    name: 'User 123 Budget',
    limit: 10,
    period: 'daily',
    scope: 'user',
    scopeId: 'user-123',
    hardLimit: true,
  };

  engine.createBudget(dailyBudget);
  engine.createBudget(userBudget);

  // Create rate limits
  const globalQuota: RateLimitQuota = {
    id: 'global',
    name: 'Global Rate Limit',
    scope: 'global',
    limits: {
      requests: 1000,
      tokens: 1000000,
      window: 3600000,
    },
  };

  const userQuota: RateLimitQuota = {
    id: 'user-123',
    name: 'User Rate Limit',
    scope: 'user',
    scopeId: 'user-123',
    limits: {
      requests: 100,
      tokens: 100000,
      window: 3600000,
    },
  };

  engine.createRateLimit(globalQuota);
  engine.createRateLimit(userQuota);

  // Execute with full options
  const request: LLMRequest = {
    messages: [
      { role: 'user', content: 'Complex analysis request' },
    ],
  };

  const response = await engine.execute(request, {
    userId: 'user-123',
    priority: 5,
    multiModel: true,
    routing: {
      strategy: 'performance',
      fallbackEnabled: true,
    },
    aggregation: {
      method: 'weighted',
      strategy: 'best',
    },
  });

  console.log('Advanced execution complete');

  engine.dispose();
}

