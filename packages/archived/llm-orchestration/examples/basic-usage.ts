// Example 1: Basic Request Execution
// ============================================================================

async function basicRequestExample() {
  const engine = createOrchestrationEngine();

  // Register a provider (in real usage, you'd use actual provider clients)
  engine.registerProvider('openai', {
    async chat(request: LLMRequest) {
      // Actual API call would go here
      return {
        id: 'resp-1',
        model: request.model || 'gpt-4',
        choices: [
          {
            index: 0,
            message: {
              role: 'assistant',
              content: 'Hello! How can I help you today?',
            },
            finishReason: 'stop',
          },
        ],
        usage: {
          promptTokens: 10,
          completionTokens: 9,
          totalTokens: 19,
        },
        created: Date.now(),
      };
    },
  });

  // Execute a simple request
  const request: LLMRequest = {
    messages: [
      { role: 'user', content: 'Hello, how are you?' },
    ],
  };

  const response = await engine.execute(request);
  console.log('Response:', response.choices[0].message.content);

  engine.dispose();
}

